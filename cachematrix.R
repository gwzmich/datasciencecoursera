## Create a function that creates a special "matrix" object that can cache its inverse
## and a function that computes the inverse of the "special" matrix

## Create matrix

makeCacheMatrix <- function(x = matrix()) {
        m <- NULL
        set <- function(y) {
                x <<- y
                m <<- NULL
        }
        get <- function() x
        setinv <- function(solve) m <<- solve
        getinv <- function() m
        list(set = set, get = get,
             setinv = setinv,
             getinv = getinv)                         
}

## Compute inverse of matrix returned by 'makeCacheMatrix'
## If inverse is already calculated, retrieve inverse from the cached matrix

cacheSolve <- function(x, ...) {
        m <- x$getinv()
        if(!is.null(m)) {
                message("getting cached data")
                return(m)
        }
        data <- x$get()
        m <- solve(data, ...)
        x$setinv(m)
        m          
}